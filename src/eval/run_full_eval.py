def main():
    parser = argparse.ArgumentParser(description="Full evaluation suite for LLaMA RAG model")
    parser.add_argument("--model_path", required=True,
                        help="Path to trained model (e.g. checkpoints/llama-rag/final)")
    parser.add_argument("--corpus_path", default=None,
                        help="Path to corpus.jsonl (default: data/processed/core/corpus.jsonl)")
    parser.add_argument("--test_splits_path", default=None,
                        help="Path to test_splits.json (default: data/processed/evaluation/test_splits.json)")
    parser.add_argument("--rag_jsonl_path", default=None,
                        help="Path to test JSONL with RAG context (with_rag) or without "
                             "(without_rag). Controls RAG vs no-RAG eval condition. "
                             "Default: data/processed/training/test_balanced_with_rag.jsonl")
    parser.add_argument("--no_rag", action="store_true",
                        help="Shorthand: use test_balanced_without_rag.jsonl instead of with_rag")
    parser.add_argument("--model_name", default=None,
                        help="Human-readable label for this condition, e.g. 'llama_rag' or 'qwen_no_rag'. "
                             "Used in W&B run name and results file. Defaults to model folder name.")
    parser.add_argument("--raid_path", default=None,
                        help="Path to raid_eval.jsonl (default: data/processed/evaluation/raid_eval.jsonl)")
    parser.add_argument("--max_seq_length", type=int, default=2048)
    parser.add_argument("--max_samples", type=int, default=None,
                        help="Cap samples per scenario (for smoke-testing)")
    parser.add_argument("--skip_adversarial", action="store_true",
                        help="Skip RAID adversarial eval (no Gemini API / time constraint)")
    parser.add_argument("--scenarios", nargs="+",
                        choices=["standard", "cross_model", "cross_platform", "short_text", "adversarial"],
                        default=["standard", "cross_model", "cross_platform"],
                        help="Scenarios to run. Default: active scenarios. Example: --scenarios standard cross_platform")
    parser.add_argument("--no_wandb", action="store_true",
                        help="Disable W&B logging")
    args = parser.parse_args()

    # Resolve default paths relative to project root
    def resolve(arg, default_rel):
        return arg if arg else os.path.join(PROJECT_ROOT, default_rel)

    corpus_path      = resolve(args.corpus_path,      "data/processed/core/corpus.jsonl")
    test_splits_path = resolve(args.test_splits_path, "data/processed/evaluation/test_splits.json")
    if args.no_rag:
        rag_jsonl_default = "data/processed/training/test_balanced_without_rag.jsonl"
    else:
        rag_jsonl_default = "data/processed/training/test_balanced_with_rag.jsonl"
    rag_jsonl_path   = resolve(args.rag_jsonl_path, rag_jsonl_default)
    raid_path        = resolve(args.raid_path, "data/processed/evaluation/raid_eval.jsonl")

    # Labels used in W&B run name and results filename
    model_exp     = args.model_name or os.path.basename(os.path.dirname(args.model_path.rstrip("/")))
    rag_condition = "no_rag" if args.no_rag else "with_rag"

    print("=" * 60)
    print("FULL EVALUATION SUITE")
    print("=" * 60)
    print(f"  Model:         {args.model_path}")
    print(f"  Condition:     {model_exp}__{rag_condition}")
    print(f"  RAG JSONL:     {rag_jsonl_path}")
    print(f"  Max samples:   {args.max_samples or 'all'}")
    print(f"  Skip RAID:     {args.skip_adversarial}")
    print("=" * 60)

    if not torch.cuda.is_available():
        raise RuntimeError("No GPU detected. Submit via SLURM: sbatch scripts/evaluate.sh")

    # Load model once, reuse across all scenarios
    model, tokenizer = load_model(args.model_path, args.max_seq_length)
    print("\nVerifying label tokens...")
    ai_token_id, human_token_id = verify_label_tokens(tokenizer)

    # Load corpus and test split index
    print("\nLoading corpus and test splits...")
    corpus = load_corpus(corpus_path)
    test_splits = load_test_splits(test_splits_path)
    print(f"  Corpus: {len(corpus)} records")
    print(f"  Test split scenarios: {list(test_splits.keys())}")
    standard_ids = test_splits.get("standard")

    all_results = {}

    # Active scenarios from test_splits.json
    for scenario in ("standard", "cross_model", "cross_platform", "short_text"):
        if scenario not in args.scenarios:
            print(f"\nScenario '{scenario}' not in --scenarios, skipping.")
            continue
        resolved_name, split_ids = resolve_split_ids(test_splits, scenario)
        if split_ids is None:
            print(f"\nScenario '{scenario}' not found in test_splits.json, skipping.")
            continue
        metrics = evaluate_split_scenario(
            scenario, split_ids, corpus, rag_jsonl_path,
            model, tokenizer, ai_token_id, human_token_id,
            args.max_seq_length, args.max_samples, standard_ids=standard_ids,
        )
        if metrics:
            all_results[scenario] = metrics

    # Scenario 5: adversarial (RAID)
    if "adversarial" in args.scenarios and not args.skip_adversarial:
        if not os.path.exists(raid_path):
            print(f"\nRAID file not found at {raid_path}, skipping adversarial eval.")
        else:
            adv_results = evaluate_adversarial(
                raid_path, model, tokenizer, ai_token_id, human_token_id,
                args.max_seq_length, args.max_samples,
            )
            all_results["adversarial"] = adv_results

    # Summary table
    print_summary_table(all_results)

    # Save results locally under the project results directory. MODEL_PATH may be a HF repo id.
    results_dir = os.path.join(PROJECT_ROOT, "results")
    os.makedirs(results_dir, exist_ok=True)
    results_path = os.path.join(results_dir, f"eval_results__{model_exp}__{rag_condition}.json")
    with open(results_path, "w") as f:
        json.dump(all_results, f, indent=2)
    print(f"\nResults saved: {results_path}")

    # W&B
    if not args.no_wandb:
        log_to_wandb(all_results, args.model_path, args, model_exp, rag_condition)

    print("\nDone.")


if __name__ == "__main__":
    main()
