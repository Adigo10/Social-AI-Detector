def build_raid_records(raid_path):
    """Load RAID records and format them as plain instruction records."""
    records = []
    with open(raid_path, "r", encoding="utf-8") as f:
        for line in f:
            rec = json.loads(line)
            records.append({
                "id": rec.get("id", ""),
                "source_model": rec.get("source_model", "unknown"),
                "instruction": (
                    "Classify the following text as 'ai' or 'human'.\n\n"
                    f"Text: {rec['text']}"
                ),
                "output": rec["label"],
            })
    return records


def evaluate_adversarial(
    raid_path, model, tokenizer, ai_token_id, human_token_id,
    max_seq_length, max_samples
):
    print(f"\n{'='*60}")
    print("Scenario: adversarial (RAID)")
    print(f"{'='*60}")

    records = build_raid_records(raid_path)
    print(f"  Loaded {len(records)} RAID records")

    y_true, y_scores, y_pred, skipped = run_inference(
        model, tokenizer, records,
        ai_token_id, human_token_id,
        max_seq_length, max_samples
    )
    if skipped:
        print(f"  Skipped: {skipped}/{len(records)} ({100*skipped/len(records):.2f}%)")

    overall = compute_metrics(y_true, y_scores, y_pred)
    overall["n_skipped"] = skipped
    _print_metrics("adversarial (overall)", overall)

    # Per source-model breakdown (aligns with raid_eval.py)
    source_models = sorted(set(r["source_model"] for r in records))
    by_source = {}
    for sm in source_models:
        mask = np.array([r["source_model"] == sm for r in records[:len(y_true)]])
        if mask.sum() == 0:
            continue
        m = compute_metrics(y_true[mask], y_scores[mask], y_pred[mask])
        by_source[sm] = m

    if by_source:
        print(f"\n  {'Source Model':<22} {'N':>6} {'TPR@1%FPR':>10} {'AUROC':>8} {'F1':>8} {'Acc':>8}")
        print("  " + "-" * 68)
        for sm in sorted(by_source):
            m = by_source[sm]
            print(
                f"  {sm:<22} {m['n_records']:>6} "
                f"{_fmt(m['tpr_at_1fpr']):>10} {_fmt(m['auroc']):>8} "
                f"{_fmt(m['macro_f1']):>8} {_fmt(m['accuracy']):>8}"
            )

    return {"overall": overall, "by_source_model": by_source}


# ---------------------------------------------------------------------------
# Printing helpers
# ---------------------------------------------------------------------------

def _print_metrics(name, m):
    print(f"\n  Results for: {name}")
    print(f"    Records:      {m['n_records']}  (AI: {m['n_ai']}, Human: {m['n_human']})")
    print(f"    TPR@1%FPR:    {_fmt(m['tpr_at_1fpr'])}  <- PRIMARY")
    print(f"    AUROC:        {_fmt(m['auroc'])}")
    print(f"    Macro-F1:     {_fmt(m['macro_f1'])}")
    print(f"    Accuracy:     {_fmt(m['accuracy'])}")


def print_summary_table(all_results):
    print(f"\n{'='*75}")
    print("FULL EVALUATION SUMMARY")
    print(f"{'='*75}")
    print(f"{'Scenario':<22} {'N':>7} {'TPR@1%FPR':>11} {'AUROC':>8} {'F1':>8} {'Acc':>8}")
    print("-" * 75)
    for name, res in all_results.items():
        if name == "adversarial":
            m = res["overall"]
        else:
            m = res
        print(
            f"{name:<22} {m['n_records']:>7} "
            f"{_fmt(m['tpr_at_1fpr']):>11} {_fmt(m['auroc']):>8} "
            f"{_fmt(m['macro_f1']):>8} {_fmt(m['accuracy']):>8}"
        )
    print(f"{'='*75}")


