#!/usr/bin/env python3
"""Full evaluation suite for the LLaMA RAG fine-tuned model.

Active scenarios:
  1. standard       - balanced held-out test split
  2. cross_model    - chatgpt subset of standard
  3. cross_platform - qa_forum subset of standard

For each scenario, reports TPR@1%FPR (primary), AUROC, Macro-F1, Accuracy.
All results logged to a single W&B eval run and saved locally as JSON.

Usage:
    python src/eval/run_full_eval.py \
        --model_path checkpoints/llama-rag/final \
        --corpus_path data/processed/core/corpus.jsonl \
        --test_splits_path data/processed/evaluation/test_splits.json \
        [--max_samples N]   # for quick smoke-test
        [--no_wandb]
        [--skip_adversarial]
"""

