def log_to_wandb(all_results, model_path, args, model_exp, rag_condition):
    import wandb

    run_name = f"eval__{model_exp}__{rag_condition}"

    # Load training run ID for traceability
    training_run_id = None
    for candidate in [
        os.path.join(model_path, "wandb_run_id.txt"),
        os.path.join(os.path.dirname(model_path), "wandb_run_id.txt"),
    ]:
        if os.path.exists(candidate):
            with open(candidate) as f:
                training_run_id = f.read().strip()
            break

    wandb.init(
        entity="social-media-detection",
        project="social-ai-detector",
        name=run_name,
        tags=["eval", "full-suite"],
        config={
            "model_path": model_path,
            "training_run_id": training_run_id,
            "rag_condition": rag_condition,
            "max_samples": args.max_samples,
            "skip_adversarial": args.skip_adversarial,
        },
    )

    metrics_flat = {}
    for scenario, res in all_results.items():
        if scenario == "adversarial":
            m = res["overall"]
            # Also log per-source breakdown
            for sm, sm_metrics in res["by_source_model"].items():
                for k, v in sm_metrics.items():
                    if isinstance(v, (int, float)) and v is not None:
                        metrics_flat[f"adversarial/{sm}/{k}"] = v
        else:
            m = res
        for k, v in m.items():
            if isinstance(v, (int, float)) and v is not None:
                metrics_flat[f"{scenario}/{k}"] = v

    wandb.log(metrics_flat)

    # Summary: primary metric per scenario (shows in runs table)
    for scenario, res in all_results.items():
        m = res["overall"] if scenario == "adversarial" else res
        if m.get("tpr_at_1fpr") is not None:
            wandb.summary[f"{scenario}/tpr_at_1fpr"] = m["tpr_at_1fpr"]

    wandb.finish()
    print(f"\nW&B run logged: {run_name}")


