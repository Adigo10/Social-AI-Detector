def load_corpus(corpus_path):
    corpus = {}
    with open(corpus_path, "r", encoding="utf-8") as f:
        for line in f:
            rec = json.loads(line)
            corpus[rec["id"]] = rec
    return corpus


def load_test_splits(test_splits_path):
    with open(test_splits_path, "r", encoding="utf-8") as f:
        return json.load(f)


def build_records_from_split(split_ids, corpus, rag_jsonl_path, standard_ids=None):
    """Build instruction-tuned records for a list of corpus IDs.

    Looks up each ID in the RAG JSONL (which has 'instruction'+'output') by
    matching on id field. If the JSONL does not contain ids but matches the
    balanced standard split in order, map those records back to the standard
    split ids so standard and short_text scenarios preserve RAG context.
    Falls back to a plain instruction if RAG file is not provided or ID is
    missing.
    """
    # Build id -> rag_record lookup if path exists
    rag_lookup = {}
    rag_records_without_ids = []
    used_legacy_positional_map = False
    if rag_jsonl_path and os.path.exists(rag_jsonl_path):
        with open(rag_jsonl_path, "r", encoding="utf-8") as f:
            for line in f:
                rec = json.loads(line)
                if "id" in rec:
                    rag_lookup[rec["id"]] = rec
                else:
                    rag_records_without_ids.append(rec)

    if standard_ids and rag_records_without_ids and len(rag_records_without_ids) == len(standard_ids):
        for sid, rec in zip(standard_ids, rag_records_without_ids):
            rag_lookup[sid] = rec
        used_legacy_positional_map = True
    elif rag_records_without_ids:
        print(
            "  Warning: prompt JSONL has no ids and could not be mapped safely. "
            "Falling back to plain prompts for unmatched records."
        )

    records = []
    missing_rag = 0
    for sid in split_ids:
        if sid in rag_lookup:
            records.append(rag_lookup[sid])
        elif sid in corpus:
            # Fallback: build a plain (no-RAG) instruction from corpus text
            cr = corpus[sid]
            records.append({
                "id": sid,
                "instruction": (
                    "Classify the following social media post as 'ai' or 'human'.\n\n"
                    f"Post: {cr['text']}"
                ),
                "output": cr["label"],
            })
            missing_rag += 1
        # else: ID not found anywhere, skip silently

    if missing_rag:
        print(f"  Warning: {missing_rag} records used plain fallback (no RAG context found).")
    if used_legacy_positional_map:
        print(
            "  Warning: using legacy positional prompt mapping. "
            "Prefer v2 prompt files with explicit ids."
        )

    return records


def resolve_split_ids(test_splits, scenario):
    """Resolve user-facing scenario names to concrete keys in test_splits.json."""
    if scenario in test_splits:
        return scenario, test_splits[scenario]

    alias_prefixes = {
        "cross_model": "cross_model_",
        "cross_platform": "cross_platform_",
    }
    prefix = alias_prefixes.get(scenario)
    if prefix:
        matches = sorted(k for k in test_splits if k.startswith(prefix))
        if len(matches) == 1:
            return matches[0], test_splits[matches[0]]
        if len(matches) > 1:
            raise ValueError(
                f"Scenario '{scenario}' is ambiguous. Matching keys: {matches}"
            )

    return None, None


def evaluate_split_scenario(
    name, split_ids, corpus, rag_jsonl_path,
    model, tokenizer, ai_token_id, human_token_id,
    max_seq_length, max_samples, standard_ids=None
):
    print(f"\n{'='*60}")
    print(f"Scenario: {name}  ({len(split_ids)} records)")
    print(f"{'='*60}")

    records = build_records_from_split(split_ids, corpus, rag_jsonl_path, standard_ids=standard_ids)
    if not records:
        print("  No records found, skipping.")
        return None

    y_true, y_scores, y_pred, skipped = run_inference(
        model, tokenizer, records,
        ai_token_id, human_token_id,
        max_seq_length, max_samples
    )
    if skipped:
        print(f"  Skipped: {skipped}/{len(records)} ({100*skipped/len(records):.2f}%)")

    metrics = compute_metrics(y_true, y_scores, y_pred)
    metrics["n_skipped"] = skipped
    _print_metrics(name, metrics)
    return metrics


