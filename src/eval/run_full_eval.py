import json
import os
import sys
import time

import numpy as np
import torch
from sklearn.metrics import accuracy_score, f1_score, roc_auc_score, roc_curve
from tqdm import tqdm

# ---------------------------------------------------------------------------
# Paths (relative to project root — script resolves at runtime)
# ---------------------------------------------------------------------------

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
PROJECT_ROOT = os.path.abspath(os.path.join(SCRIPT_DIR, "..", ".."))


# ---------------------------------------------------------------------------
# Metrics (shared across all scenarios)
# ---------------------------------------------------------------------------

def compute_metrics(y_true, y_scores, y_pred):
    """Return TPR@1%FPR, AUROC, Macro-F1, Accuracy. Handles degenerate splits."""
    n_pos = int(y_true.sum())
    n_neg = int(len(y_true) - n_pos)
    if n_pos == 0 or n_neg == 0:
        return {
            "n_records": len(y_true),
            "n_ai": n_pos,
            "n_human": n_neg,
            "accuracy": round(float(accuracy_score(y_true, y_pred)), 4),
            "macro_f1": None,
            "auroc": None,
            "tpr_at_1fpr": None,
        }
    fpr, tpr, _ = roc_curve(y_true, y_scores)
    return {
        "n_records": len(y_true),
        "n_ai": n_pos,
        "n_human": n_neg,
        "accuracy": round(float(accuracy_score(y_true, y_pred)), 4),
        "macro_f1": round(float(f1_score(y_true, y_pred, average="macro")), 4),
        "auroc": round(float(roc_auc_score(y_true, y_scores)), 4),
        "tpr_at_1fpr": round(float(np.interp(0.01, fpr, tpr)), 4),
    }


def _fmt(v):
    return "N/A" if v is None else f"{v:.4f}"


# ---------------------------------------------------------------------------
# Model loading
# ---------------------------------------------------------------------------

def load_model(model_path, max_seq_length):
    print(f"\nLoading model from: {model_path}")
    from unsloth import FastLanguageModel
    model, tokenizer = FastLanguageModel.from_pretrained(
        model_name=model_path,
        max_seq_length=max_seq_length,
        dtype=None,
        load_in_4bit=True,
    )
    model.eval()
    print("Model loaded.")
    return model, tokenizer


def verify_label_tokens(tokenizer):
    """Fail fast if 'ai' or 'human' are not single tokens."""
    for label in ("ai", "human"):
        ids = tokenizer(label, add_special_tokens=False).input_ids
        if len(ids) != 1:
            raise ValueError(
                f"'{label}' tokenizes to {len(ids)} tokens {ids}. "
                "Labels must be single tokens — check training data format."
            )
    ai_id = tokenizer("ai", add_special_tokens=False).input_ids[0]
    human_id = tokenizer("human", add_special_tokens=False).input_ids[0]
    print(f"  'ai'    -> token {ai_id}  ('{tokenizer.decode([ai_id])}')")
    print(f"  'human' -> token {human_id} ('{tokenizer.decode([human_id])}')")
    return ai_id, human_id


# ---------------------------------------------------------------------------
# Prompt formatting (must match train_llama_rag.py exactly)
# ---------------------------------------------------------------------------

SYSTEM_PROMPT = (
    "You are a classifier that determines whether a social media post was "
    "written by a human or generated by AI. Respond with exactly one word: "
    "'ai' or 'human'."
)

INSTRUCTION_PREAMBLE = (
    "You are a classifier that determines whether a social media post was "
    "written by a human or generated by AI.\n\n"
)


def format_eval_prompt(instruction, tokenizer):
    """Format an instruction into the chat template used during training.

    Mirrors format_sample() in train_llama_rag.py exactly:
    strips the preamble, puts remainder as user message under SYSTEM_PROMPT,
    then applies the chat template with add_generation_prompt=True.
    """
    if instruction.startswith(INSTRUCTION_PREAMBLE):
        user_content = instruction[len(INSTRUCTION_PREAMBLE):]
    else:
        parts = instruction.split("\n\n", 1)
        user_content = parts[1] if len(parts) > 1 else instruction

    messages = [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": user_content},
    ]
    return tokenizer.apply_chat_template(
        messages, tokenize=False, add_generation_prompt=True
    )


# ---------------------------------------------------------------------------
# Inference
# ---------------------------------------------------------------------------

def predict_single(model, tokenizer, instruction, ai_token_id, human_token_id, max_seq_length):
    """Single-example inference. Returns (prediction, ai_confidence)."""
    prompt = format_eval_prompt(instruction, tokenizer)
    inputs = tokenizer(
        prompt, return_tensors="pt", truncation=True, max_length=max_seq_length
    ).to(model.device)

    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens=1,
            output_scores=True,
            return_dict_in_generate=True,
            do_sample=False,
            use_cache=True,
            pad_token_id=tokenizer.pad_token_id,
            eos_token_id=tokenizer.eos_token_id,
        )

    logits = outputs.scores[0][0]
    probs = torch.softmax(logits, dim=-1)
    ai_prob = probs[ai_token_id].item()
    generated_id = outputs.sequences[0][-1].item()

    if generated_id == ai_token_id:
        pred = "ai"
    elif generated_id == human_token_id:
        pred = "human"
    else:
        decoded = tokenizer.decode([generated_id])
        print(f"\nWarning: unexpected token '{decoded}' (id={generated_id}), using prob threshold.")
        pred = "ai" if ai_prob > 0.5 else "human"

    return pred, ai_prob


def run_inference(model, tokenizer, records, ai_token_id, human_token_id,
                  max_seq_length, max_samples=None):
    """Run inference over a list of records with 'instruction' and 'output' fields.

    Returns (y_true, y_scores, y_pred, n_skipped).
    """
    if max_samples is not None:
        records = records[:max_samples]

    y_true, y_pred, y_scores = [], [], []
    skipped = 0

    for example in tqdm(records, desc="Inferring"):
        instruction = example["instruction"]
        true_output = example["output"].strip().lower()
        try:
            pred, conf = predict_single(
                model, tokenizer, instruction,
                ai_token_id, human_token_id, max_seq_length
            )
        except Exception as e:
            print(f"\nSkipping example due to error: {e}")
            skipped += 1
            continue

        y_true.append(1 if true_output == "ai" else 0)
        y_pred.append(1 if pred == "ai" else 0)
        y_scores.append(conf)

    return np.array(y_true), np.array(y_scores), np.array(y_pred), skipped


