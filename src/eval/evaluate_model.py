#!/usr/bin/env python3
"""
Evaluate trained model on validation set.

Calculates TPR @ 1% FPR and other RAID benchmark metrics.
Logs to W&B as a separate evaluation run (does not pollute training run).
"""

import argparse
import json
import os
from typing import Dict, Tuple

import numpy as np
import torch
import unsloth
from datasets import load_dataset
from sklearn.metrics import accuracy_score, f1_score, roc_auc_score, roc_curve
from tqdm import tqdm
from unsloth import FastLanguageModel


def load_wandb_run_id(checkpoint_dir):
    """Load W&B run ID from training checkpoint."""
    run_id_file = os.path.join(checkpoint_dir, "wandb_run_id.txt")
    if os.path.exists(run_id_file):
        with open(run_id_file, "r") as f:
            run_id = f.read().strip()
        print(f"Found W&B run ID: {run_id}")
        return run_id

    # Check parent directory
    parent_dir = os.path.dirname(checkpoint_dir)
    parent_run_id_file = os.path.join(parent_dir, "wandb_run_id.txt")
    if os.path.exists(parent_run_id_file):
        with open(parent_run_id_file, "r") as f:
            run_id = f.read().strip()
        print(f"Found W&B run ID in parent dir: {run_id}")
        return run_id

    return None


SYSTEM_PROMPT = (
    "You are a classifier that determines whether a social media post was "
    "written by a human or generated by AI. Respond with exactly one word: "
    "'ai' or 'human'."
)

INSTRUCTION_PREAMBLE = (
    "You are a classifier that determines whether a social media post was "
    "written by a human or generated by AI.\n\n"
)


def format_prompt(instruction: str, tokenizer) -> str:
    """Format instruction using the same chat template as training.

    Mirrors format_sample() in train_llama_rag.py: strips the preamble,
    puts remainder as user message, applies chat template with
    add_generation_prompt=True.
    """
    if instruction.startswith(INSTRUCTION_PREAMBLE):
        user_content = instruction[len(INSTRUCTION_PREAMBLE) :]
    else:
        parts = instruction.split("\n\n", 1)
        user_content = parts[1] if len(parts) > 1 else instruction

    messages = [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": user_content},
    ]
    return tokenizer.apply_chat_template(
        messages, tokenize=False, add_generation_prompt=True
    )


def get_label_tokens(tokenizer) -> Tuple[int, int]:
    """
    Verify that both 'ai' and 'human' are single tokens and return their IDs.
    Call ONCE at startup — fails fast before wasting time on 74K examples.

    Returns:
        (ai_token_id, human_token_id)
    """
    for label in ("ai", "human"):
        token_ids = tokenizer(label, add_special_tokens=False).input_ids
        if len(token_ids) != 1:
            raise ValueError(
                f"'{label}' tokenizes to {len(token_ids)} tokens {token_ids}, expected 1. "
                f"Decoded: {[tokenizer.decode([t]) for t in token_ids]}. "
                "Adjust the label format used during training so both labels are single tokens."
            )

    ai_token_id = tokenizer("ai", add_special_tokens=False).input_ids[0]
    human_token_id = tokenizer("human", add_special_tokens=False).input_ids[0]
    print(f"  'ai'    → token ID {ai_token_id} ('{tokenizer.decode([ai_token_id])}')")
    print(
        f"  'human' → token ID {human_token_id} ('{tokenizer.decode([human_token_id])}')"
    )
    return ai_token_id, human_token_id


def get_model_prediction_with_confidence(
    model,
    tokenizer,
    instruction: str,
    ai_token_id: int,
    human_token_id: int,
    max_seq_length: int,
) -> Tuple[str, float]:
    """
    Get model prediction and confidence score for a single example.

    Uses a single generate() call with max_new_tokens=1 and output_scores=True.

    Why not model(**inputs).logits[0, -1, :]?
      The last INPUT position predicts the next token under teacher-forcing,
      but it does so without the KV cache or generation-mode attention mask that
      model.generate() uses. Using generate() with output_scores=True gives the
      logits from the actual first generation step — consistent with inference.

    Returns:
        (prediction, confidence) where prediction is "ai"/"human"
        and confidence is P("ai" token) at the first generation step.
    """
    prompt = format_prompt(instruction, tokenizer)
    inputs = tokenizer(
        prompt, return_tensors="pt", truncation=True, max_length=max_seq_length
    ).to(model.device)

    with torch.no_grad():
        # Single call: generate exactly 1 token and capture its scores.
        # output_scores=True → outputs.scores is a tuple of length max_new_tokens,
        # each element has shape [batch_size, vocab_size].
        outputs = model.generate(
            **inputs,
            max_new_tokens=1,
            output_scores=True,
            return_dict_in_generate=True,
            do_sample=False,
            use_cache=True,
            pad_token_id=tokenizer.pad_token_id,
            eos_token_id=tokenizer.eos_token_id,
        )

    # Logits for the first (and only) generated token, batch index 0.
    first_token_logits = outputs.scores[0][0]  # [vocab_size]
    probs = torch.softmax(first_token_logits, dim=-1)
    ai_probability = probs[ai_token_id].item()

    # Hard prediction: compare the generated token ID directly — no string parsing.
    generated_id = outputs.sequences[0][-1].item()

    if generated_id == ai_token_id:
        prediction = "ai"
    elif generated_id == human_token_id:
        prediction = "human"
    else:
        decoded = tokenizer.decode([generated_id])
        print(
            f"\nWarning: unexpected first token '{decoded}' (id={generated_id}), skipping."
        )
        return None, None

    return prediction, ai_probability


def compute_metrics(y_true, y_scores, y_pred):
    """Compute TPR@1%FPR, AUROC, Macro-F1, and Accuracy."""
    n_pos = int(y_true.sum())
    n_neg = int(len(y_true) - n_pos)

    # Need both classes for meaningful metrics
    if n_pos == 0 or n_neg == 0:
        return {
            "n_records": len(y_true),
            "n_ai": n_pos,
            "n_human": n_neg,
            "accuracy": round(float(accuracy_score(y_true, y_pred)), 4),
            "macro_f1": None,
            "auroc": None,
            "tpr_at_1fpr": None,
        }

    accuracy = accuracy_score(y_true, y_pred)
    macro_f1 = f1_score(y_true, y_pred, average="macro")
    auroc = roc_auc_score(y_true, y_scores)

    # TPR @ 1% FPR (RAID benchmark standard)
    fpr, tpr, _ = roc_curve(y_true, y_scores)
    tpr_at_1fpr = float(np.interp(0.01, fpr, tpr))

    return {
        "n_records": len(y_true),
        "n_ai": n_pos,
        "n_human": n_neg,
        "accuracy": round(accuracy, 4),
        "macro_f1": round(macro_f1, 4),
        "auroc": round(auroc, 4),
        "tpr_at_1fpr": round(tpr_at_1fpr, 4),
    }


def evaluate_model(
    model,
    tokenizer,
    eval_dataset,
    ai_token_id: int,
    human_token_id: int,
    max_seq_length: int,
    max_samples: int = None,
) -> Dict:
    """
    Evaluate model on validation dataset.

    Returns:
        Dictionary with RAID benchmark metrics (TPR@1%FPR, AUROC, etc.)
    """
    predictions = []
    true_labels = []
    ai_scores = []
    skipped = 0

    samples = (
        eval_dataset
        if max_samples is None
        else eval_dataset.select(range(min(max_samples, len(eval_dataset))))
    )

    print(f"\nEvaluating on {len(samples)} examples...")

    for i, example in enumerate(tqdm(samples, desc="Evaluating")):
        instruction = example["instruction"]
        true_output = example["output"].strip().lower()

        try:
            prediction, ai_confidence = get_model_prediction_with_confidence(
                model,
                tokenizer,
                instruction,
                ai_token_id,
                human_token_id,
                max_seq_length,
            )
        except Exception as e:
            print(f"\nWarning: skipping example {i}, error: {e}")
            skipped += 1
            continue

        if prediction is None:
            skipped += 1
            continue

        predictions.append(1 if prediction == "ai" else 0)
        true_labels.append(1 if true_output == "ai" else 0)
        ai_scores.append(ai_confidence)

    if skipped > 0:
        skip_pct = 100 * skipped / len(samples)
        print(
            f"\n⚠️  Skipped {skipped}/{len(samples)} examples ({skip_pct:.2f}%) due to errors."
        )
        if skip_pct > 0.1:
            print("   WARNING: >0.1% skip rate — investigate before trusting results.")

    # Convert to numpy arrays
    y_true = np.array(true_labels)
    y_pred = np.array(predictions)
    y_scores = np.array(ai_scores)

    # Compute RAID benchmark metrics
    metrics = compute_metrics(y_true, y_scores, y_pred)

    # Add W&B-friendly prefixes
    metrics_wandb = {
        "eval/tpr_at_1fpr": metrics["tpr_at_1fpr"],  # PRIMARY METRIC
        "eval/accuracy": metrics["accuracy"],
        "eval/auroc": metrics["auroc"],
        "eval/macro_f1": metrics["macro_f1"],
        "eval/n_samples": metrics["n_records"],
        "eval/n_ai": metrics["n_ai"],
        "eval/n_human": metrics["n_human"],
        "eval/n_skipped": skipped,
    }

    print(f"\n{'=' * 60}")
    print("EVALUATION RESULTS")
    print(f"{'=' * 60}")
    print(f"Total samples: {metrics['n_records']}")
    print(f"AI examples:   {metrics['n_ai']}")
    print(f"Human examples:{metrics['n_human']}")
    print(f"Skipped:       {skipped}")
    print(f"")
    print(f"TPR @ 1% FPR:  {metrics['tpr_at_1fpr']}  ← PRIMARY METRIC")
    print(f"Accuracy:      {metrics['accuracy']}")
    print(f"AUROC:         {metrics['auroc']}")
    print(f"Macro F1:      {metrics['macro_f1']}")
    print(f"{'=' * 60}\n")

    return metrics_wandb


def main():
    parser = argparse.ArgumentParser(
        description="Evaluate trained model on validation set"
    )
    parser.add_argument(
        "--model_path",
        type=str,
        required=True,
        help="Path to trained model directory (final_model or checkpoint)",
    )
    parser.add_argument(
        "--val_file",
        type=str,
        default="data/processed/training/val_balanced_with_rag.jsonl",
        help="Path to validation JSONL file",
    )
    parser.add_argument(
        "--max_samples",
        type=int,
        default=None,
        help="Maximum number of samples to evaluate (for testing)",
    )
    parser.add_argument(
        "--max_seq_length", type=int, default=2048, help="Maximum sequence length"
    )
    parser.add_argument("--no_wandb", action="store_true", help="Disable W&B logging")
    parser.add_argument(
        "--wandb_run_id",
        type=str,
        default=None,
        help="W&B run ID (overrides auto-detection)",
    )
    args = parser.parse_args()

    print("=" * 60)
    print("MODEL EVALUATION")
    print("=" * 60)
    print(f"Model path:     {args.model_path}")
    print(f"Validation file:{args.val_file}")
    print(f"Max samples:    {args.max_samples or 'All'}")
    print("=" * 60)

    if not torch.cuda.is_available():
        raise RuntimeError(
            "No GPU detected. This script requires a CUDA GPU. "
            "If you are on a login node, submit via SLURM: sbatch evaluate_slurm.sh"
        )

    # ========================================================================
    # LOAD MODEL
    # ========================================================================
    print("\nLoading model...")

    model, tokenizer = FastLanguageModel.from_pretrained(
        model_name=args.model_path,
        max_seq_length=args.max_seq_length,
        dtype=None,
        load_in_4bit=True,
    )
    model.eval()
    print("Model loaded successfully!")

    # ========================================================================
    # VERIFY TOKEN IDs (fail fast before running 74K examples)
    # ========================================================================
    print("\nVerifying tokenization...")
    ai_token_id, human_token_id = get_label_tokens(tokenizer)

    # ========================================================================
    # LOAD VALIDATION DATASET
    # ========================================================================
    print("\nLoading validation dataset...")
    val_dataset = load_dataset("json", data_files=args.val_file)
    val_dataset = val_dataset["train"]
    print(f"Validation dataset size: {len(val_dataset)}")

    # ========================================================================
    # RUN EVALUATION
    # ========================================================================
    metrics = evaluate_model(
        model,
        tokenizer,
        val_dataset,
        ai_token_id,
        human_token_id,
        args.max_seq_length,
        max_samples=args.max_samples,
    )

    # ========================================================================
    # LOG TO W&B (separate eval run — does not pollute training run)
    # ========================================================================
    val_stem = os.path.splitext(os.path.basename(args.val_file))[0]

    if not args.no_wandb:
        print("\nLogging to W&B...")
        import wandb

        # Derive a readable run name from experiment folder and val file
        # e.g. "eval__llama_lr0.0001_r32_ep1_20260404_191039__val_without_rag"
        # Uses the parent folder name (experiment) instead of "final_model"
        model_exp_name = os.path.basename(os.path.dirname(args.model_path.rstrip("/")))
        run_name = f"eval__{model_exp_name}__{val_stem}"

        # Link back to the training run via config, not by resuming it.
        # Resuming a training run appends eval metrics to its timeline,
        # which corrupts the loss curves and makes comparison harder.
        training_run_id = args.wandb_run_id or load_wandb_run_id(args.model_path)

        wandb.init(
            entity="social-media-detection",
            project="social-ai-detector",
            name=run_name,
            tags=["eval"],
            config={
                "model_path": args.model_path,
                "val_file": args.val_file,
                "max_samples": args.max_samples,
                "training_run_id": training_run_id,  # traceability back to training
            },
        )

        wandb.log(metrics)

        # Summary makes metrics appear in the W&B runs table for easy comparison
        wandb.summary["tpr_at_1fpr"] = metrics["eval/tpr_at_1fpr"]
        wandb.summary["auroc"] = metrics["eval/auroc"]
        wandb.summary["macro_f1"] = metrics["eval/macro_f1"]
        wandb.summary["accuracy"] = metrics["eval/accuracy"]
        wandb.summary["model_path"] = args.model_path
        wandb.summary["val_file"] = args.val_file

        wandb.finish()
        print(f"✅ Evaluation metrics logged to W&B run: {run_name}")

    # ========================================================================
    # SAVE RESULTS LOCALLY
    # ========================================================================
    results_path = os.path.join(args.model_path, f"eval_results_{val_stem}.json")
    with open(results_path, "w") as f:
        json.dump(metrics, f, indent=2)
    print(f"\n✅ Results saved to: {results_path}")

    print(f"\n{'=' * 60}")
    print("PRIMARY METRIC: TPR @ 1% FPR = {}".format(metrics["eval/tpr_at_1fpr"]))
    print(f"{'=' * 60}\n")


if __name__ == "__main__":
    main()
