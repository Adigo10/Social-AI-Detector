#!/usr/bin/env python3
"""Regenerate active validation/test prompts from current clean split ids.

Purpose:
- keep the current clean balanced validation/test ids unchanged
- rebuild prompt text fresh from corpus + retrieval index
- stop reusing inherited stale RAG prompt rows

Outputs:
- val_balanced_with_rag.jsonl
- val_balanced_without_rag.jsonl
- test_balanced_with_rag.jsonl
- test_balanced_without_rag.jsonl

The script detects either local repo layout:
  processed/core, processed/training/balanced
or cluster layout:
  data/processed/core, data/processed/training
"""

from __future__ import annotations

import argparse
import json
import shutil
from datetime import datetime
from pathlib import Path

import faiss
import numpy as np


K_NEIGHBORS = 10
K_SEARCH = K_NEIGHBORS + 1
K_RAG_CONTEXT = 5
MAX_NEIGHBOR_CHARS = 300
PROGRESS_INTERVAL = 1000


def detect_processed_root(project_root: Path) -> Path:
    for candidate in (project_root / "processed", project_root / "data" / "processed"):
        if (candidate / "core" / "corpus.jsonl").exists() or (candidate / "corpus.jsonl").exists():
            return candidate
    raise FileNotFoundError("Could not find processed root with core/corpus.jsonl")


def detect_core_dir(processed_root: Path) -> Path:
    nested = processed_root / "core"
    if (nested / "corpus.jsonl").exists():
        return nested
    if (processed_root / "corpus.jsonl").exists():
        return processed_root
    raise FileNotFoundError("Could not find corpus.jsonl in processed root")


def detect_training_output_dir(processed_root: Path) -> Path:
    balanced_dir = processed_root / "training" / "balanced"
    if balanced_dir.exists():
        return balanced_dir
    flat_dir = processed_root / "training"
    if flat_dir.exists():
        return flat_dir
    raise FileNotFoundError("Could not find training output directory")


def load_json(path: Path):
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)


def load_corpus(corpus_path: Path):
    records = []
    id_to_index = {}
    with corpus_path.open("r", encoding="utf-8") as f:
        for idx, line in enumerate(f):
            rec = json.loads(line)
            records.append(rec)
            id_to_index[rec["id"]] = idx
    return records, id_to_index


def build_rag_instruction(target_text: str, neighbors: list[tuple[str, str]]) -> str:
    context_parts = []
    for i, (text, label) in enumerate(neighbors[:K_RAG_CONTEXT], 1):
        truncated = text[:MAX_NEIGHBOR_CHARS]
        if len(text) > MAX_NEIGHBOR_CHARS:
            truncated += "..."
        context_parts.append(f"Example {i} (label: {label}):\n{truncated}")

    context_block = "\n\n".join(context_parts)
    return (
        "You are a classifier that determines whether a social media post was written by "
        "a human or generated by AI.\n\n"
        "Here are some similar texts and their labels for reference:\n\n"
        f"{context_block}\n\n"
        "Now classify the following post as either 'ai' or 'human'.\n\n"
        f"Post: {target_text}"
    )


def build_plain_instruction(target_text: str) -> str:
    return (
        "You are a classifier that determines whether a social media post was written by "
        "a human or generated by AI.\n\n"
        "Classify the following post as either 'ai' or 'human'.\n\n"
        f"Post: {target_text}"
    )


def choose_index_files(core_dir: Path) -> tuple[Path, Path]:
    balanced_index = core_dir / "corpus_balanced.index"
    balanced_map = core_dir / "train_indices_balanced.npy"
    if balanced_index.exists() and balanced_map.exists():
        return balanced_index, balanced_map

    default_index = core_dir / "corpus.index"
    default_map = core_dir / "train_indices.npy"
    if default_index.exists() and default_map.exists():
        return default_index, default_map

    raise FileNotFoundError("Could not find retrieval index files")


def maybe_move_index_to_gpu(index):
    """Use GPU FAISS if available in the installed FAISS build."""
    has_gpu = (
        hasattr(faiss, "StandardGpuResources")
        and hasattr(faiss, "index_cpu_to_gpu")
        and hasattr(faiss, "get_num_gpus")
    )
    if not has_gpu:
        print("FAISS GPU support not available in this install, using CPU index.")
        return index

    num_gpus = faiss.get_num_gpus()
    if num_gpus <= 0:
        print("FAISS GPU build found, but no GPU visible, using CPU index.")
        return index

    print(f"Moving FAISS index to GPU 0 (visible GPUs: {num_gpus})...")
    res = faiss.StandardGpuResources()
    gpu_index = faiss.index_cpu_to_gpu(res, 0, index)
    print("FAISS index is on GPU.")
    return gpu_index


def backup_if_exists(path: Path, suffix: str) -> None:
    if not path.exists():
        return
    backup_path = path.with_name(f"{path.name}.{suffix}.bak")
    shutil.copy2(path, backup_path)


def generate_rows(
    split_name: str,
    split_ids: list[int],
    records: list[dict],
    id_to_index: dict[int, int],
    embeddings: np.ndarray,
    search_index,
    local_to_corpus: list[int],
):
    with_rows = []
    without_rows = []

    print(f"\nGenerating {split_name} prompts for {len(split_ids)} records...")
    for progress, rid in enumerate(split_ids, start=1):
        corpus_idx = id_to_index[rid]
        record = records[corpus_idx]
        target_text = record["text"]
        target_label = record["label"]

        query = embeddings[corpus_idx : corpus_idx + 1].copy()
        faiss.normalize_L2(query)
        scores, neighbor_indices = search_index.search(query, K_SEARCH)

        neighbors = []
        for local_idx in neighbor_indices[0]:
            if local_idx < 0 or local_idx >= len(local_to_corpus):
                continue
            neighbor_corpus_idx = local_to_corpus[local_idx]
            if neighbor_corpus_idx == corpus_idx:
                continue
            neighbor = records[neighbor_corpus_idx]
            neighbors.append((neighbor["text"], neighbor["label"]))
            if len(neighbors) >= K_NEIGHBORS:
                break

        with_rows.append(
            {
                "id": rid,
                "instruction": build_rag_instruction(target_text, neighbors),
                "output": target_label,
            }
        )
        without_rows.append(
            {
                "id": rid,
                "instruction": build_plain_instruction(target_text),
                "output": target_label,
            }
        )

        if progress % PROGRESS_INTERVAL == 0 or progress == len(split_ids):
            print(f"  [{progress}/{len(split_ids)}] {100*progress/len(split_ids):.1f}%")

    return with_rows, without_rows


def write_jsonl(path: Path, rows: list[dict]) -> None:
    with path.open("w", encoding="utf-8") as f:
        for row in rows:
            f.write(json.dumps(row, ensure_ascii=False) + "\n")


def parse_args():
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--limit-per-split",
        type=int,
        default=None,
        help="Optional smoke-test limit for validation and standard ids.",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Generate rows but do not overwrite active JSONL outputs.",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    project_root = Path(__file__).resolve().parents[2]
    processed_root = detect_processed_root(project_root)
    core_dir = detect_core_dir(processed_root)
    evaluation_dir = processed_root / "evaluation"
    training_out_dir = detect_training_output_dir(processed_root)

    split_metadata_path = evaluation_dir / "balanced_eval_splits.json"
    if not split_metadata_path.exists():
        raise FileNotFoundError(f"Missing split metadata: {split_metadata_path}")

    split_metadata = load_json(split_metadata_path)
    validation_ids = split_metadata["validation"]
    standard_ids = split_metadata["standard"]
    if args.limit_per_split is not None:
        validation_ids = validation_ids[: args.limit_per_split]
        standard_ids = standard_ids[: args.limit_per_split]

    corpus_path = core_dir / "corpus.jsonl"
    embeddings_path = core_dir / "embeddings.npy"
    index_path, train_map_path = choose_index_files(core_dir)

    print(f"Processed root: {processed_root}")
    print(f"Training output dir: {training_out_dir}")
    print(f"Using index: {index_path.name}")
    print(f"Using mapping: {train_map_path.name}")

    records, id_to_index = load_corpus(corpus_path)
    print(f"Loaded corpus: {len(records)} rows")

    embeddings = np.load(embeddings_path)
    print(f"Loaded embeddings: {embeddings.shape}")
    if len(records) != embeddings.shape[0]:
        raise ValueError(
            f"Corpus/embedding size mismatch: {len(records)} vs {embeddings.shape[0]}"
        )

    search_index = faiss.read_index(str(index_path))
    search_index = maybe_move_index_to_gpu(search_index)
    local_to_corpus = list(np.load(train_map_path))
    if search_index.ntotal != len(local_to_corpus):
        raise ValueError(
            f"Index/mapping mismatch: {search_index.ntotal} vs {len(local_to_corpus)}"
        )

    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    out_paths = {
        "val_with": training_out_dir / "val_balanced_with_rag.jsonl",
        "val_without": training_out_dir / "val_balanced_without_rag.jsonl",
        "test_with": training_out_dir / "test_balanced_with_rag.jsonl",
        "test_without": training_out_dir / "test_balanced_without_rag.jsonl",
    }
    if not args.dry_run:
        for path in out_paths.values():
            backup_if_exists(path, f"pre_fresh_rag_{timestamp}")

    val_with_rows, val_without_rows = generate_rows(
        "validation",
        validation_ids,
        records,
        id_to_index,
        embeddings,
        search_index,
        local_to_corpus,
    )
    test_with_rows, test_without_rows = generate_rows(
        "standard_test",
        standard_ids,
        records,
        id_to_index,
        embeddings,
        search_index,
        local_to_corpus,
    )

    if args.dry_run:
        print("\nDry run only, no files written.")
    else:
        write_jsonl(out_paths["val_with"], val_with_rows)
        write_jsonl(out_paths["val_without"], val_without_rows)
        write_jsonl(out_paths["test_with"], test_with_rows)
        write_jsonl(out_paths["test_without"], test_without_rows)

    print("\nDone.")
    if args.dry_run:
        print(f"  validation rows generated: {len(val_with_rows)}")
        print(f"  standard rows generated: {len(test_with_rows)}")
    else:
        for key, path in out_paths.items():
            print(f"  {key}: {path}")


if __name__ == "__main__":
    main()
